import React, {Component} from 'react';
import {
  StyleSheet,
  Text,
  View,
  processColor,
} from 'react-native';

import {LineChart} from 'react-native-charts-wrapper';

class LineChartScreen extends React.Component {

  constructor() {
    super();
    this.state = {
      data: {},
      marker: {
        enabled: true,
        backgroundTint: processColor('teal'),
	      markerColor: processColor('#F0C0FF8C'),
        textColor: processColor('white'),

      },
      xAxis: {
        valueFormatter: ['Q1', 'Q2', 'Q3', 'Q4'],
        drawGridLines: false,
        drawAxisLine: false,
        position: 'BOTTOM',
      },
      yAxis: {
        left: {
          drawLabels: true,
          drawAxisLine: false,
          drawGridLines: false,
        },
        right: {
          enabled: false
        }
      }
    };
  }

  componentDidMount() {
    const data = {
      dataSets: [
        {
          // values: [{y: 90}, {y: 130}, {y: 100}, {y: 105}],
          values: history.prices.map(p => ({y: +p})).slice(0, 200),
          label: 'Company Y',
          config: {
            lineWidth: 1,
            drawCircles: false,
            drawCubicIntensity: 0.4,
            circleRadius: 2,
            drawHighlightIndicators: false,
            color: processColor('blue'),
            drawFilled: true,
            fillAlpha: 45,
            fillColor: processColor('blue'),
            circleColor: processColor('blue'),
            // dashedLine: { lineLength: 20, spaceLength: 10 }
          }
        },
      ],
    };
    this.setState({data});
  }

  handleSelect(event) {
    let entry = event.nativeEvent
    if (entry == null) {
      this.setState({...this.state, selectedEntry: null})
    } else {
      this.setState({...this.state, selectedEntry: JSON.stringify(entry)})
    }
  }

  render() {

    return (
      <View style={{flex: 1}}>

        <View style={styles.container}>
          <LineChart
            style={styles.chart}
            data={this.state.data}
            chartDescription={{enabled: false}}
            legend={{enabled: false}}
            marker={this.state.marker}
            xAxis={this.state.xAxis}
            yAxis={this.state.yAxis}
            drawGridBackground={false}
            borderColor={processColor('teal')}
            borderWidth={1}
            drawBorders={false}
            maxVisibleValueCount={5}
            autoScaleMinMaxEnabled={true}

            touchEnabled={true}
            dragEnabled={true}
            scaleEnabled={false}
            scaleXEnabled={true}
            scaleYEnabled={false}
            pinchZoom={true}
            doubleTapToZoomEnabled={true}

            dragDecelerationEnabled={true}
            dragDecelerationFrictionCoef={0.99}

            keepPositionOnRotation={false}
            onSelect={this.handleSelect.bind(this)}
          />
        </View>

        <View style={{height:50, position: 'absolute', backgroundColor: 'transparent'}}>
          <Text> {this.state.selectedEntry}</Text>
        </View>

      </View>
    );
  }
}

